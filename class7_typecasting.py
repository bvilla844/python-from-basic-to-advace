# converting data type
# typecasting from string to int
number = input("enter a number: ")
double_number = int(number) * 2
print(f"your number multiplied by 2 is {double_number}")

# typecasting from int to float
amount = 22
amount_float = float(amount)
print(amount_float)

# typecasting from int to str
number = 22
str_number = str(number)
print(str_number)

# typecasting from str to bool
is_valid = "yeah"
is_valid_bool = bool(is_valid)
print(is_valid_bool)

