# string concatenation
first_name = "john"
last_name = "deep"
full_name = first_name + " " + last_name
print(full_name)

# string repetition
name = "john"
print(name * 5)

# common string method
text = "hello, john"
print(text.upper())
print(text.lower())
print(text.capitalize())
print(text.title())
print(text.split())
print(len(text))
print(text.find("lo"))

# indexing and slicing
print(text[2])
print(text[-1])
print(text[2:4])
print(text[:4])