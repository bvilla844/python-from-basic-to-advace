# Introduction to Conditional Statements: Conditional statements are used
# when different actions need to be taken based on whether a specified
# condition evaluates to True or False .

# if statements
x = "hello"
if x:
    print("theres something")

# else statements
x = 6
if x % 2 == 0:
    print("even")
else:
    print("odd")

# elif statements
x = 55
if x > 80:
    print("A")
elif 70 < x < 80:
    print("B")
else:
    print("C")

