# exponentiation \ the power of a number
a = 2 ** 3
print(a, "-> data type: ", type(a))

# module operator \ return the reminder when a number is divided by other number
a = 33 % 2
print(a, "-> data type: ", type(a))

# floor division \ return the reminder complete integer number of the divisiom
a = 33 // 2
print(a, "-> data type: ", type(a))
